package Gun13;

public class _05_WhileLoop {
    public static void main(String[] args) {
        //100e kadar olan sayiların toplamını yazınız



        int toplam=0;
        int sayac=1;
        while(sayac<=100){
                toplam=toplam+sayac;
            sayac++;
        }
        System.out.println("toplam = " + toplam);




    }
}
