package Gun14;

public class _02_ForLoop {
    public static void main(String[] args) {

        int i = 0;
        while (i<=5)
        {
            System.out.println("w_i = " + i);
            i++;

        }

        // for döngüsü başı belli  sonu belli artışı belli ise kullanılır
        for (i=1; i<=5; i++){
            System.out.println("f_i = " + i);
        }
    }
}
